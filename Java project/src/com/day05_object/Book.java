package com.day05_object;

public class Book {
	private String title;//力格
	private String writer;//历磊
	//积己磊
	public Book(String title,String writer) {
		this.title=title;
		this.writer=writer;
	}
	//getter
	public String getTitle() {
		return title;
	}
	public String getWriter() {
		return writer;
	}

	
	
	
}
